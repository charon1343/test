void C_mfcDlg::OnBnClickedButtonYn()
{
	// TODO: 여기에 컨트롤 알림 처리기 코드를 추가합니다.
	int iResults;
	iResults = AfxMessageBox(_T("YES/NO 버틀을 누르셨습니다"),(MB_YESNO|MB_ICONEXCLAMATION));

	if(iResults == IDYES)
	{
		m_strResult = _T("YES 버튼을 누르셨습니다.");
		UpdateData(FALSE);
	}
	if(iResults == IDNO)
	{
		m_strResult = _T("NO버튼을 누르셨습니다!");
		UpdateData(FALSE);
	}
}


